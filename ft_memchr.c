#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*str;
	size_t				i;

	str = s;
	i = 0;
	while (*str != c && str && i < n)
	{
		str++;
		i++;
	}
	if (*str == c)
		return ((char *)str);
	return (0);
}
